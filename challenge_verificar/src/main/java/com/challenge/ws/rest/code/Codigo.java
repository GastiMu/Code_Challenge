package com.challenge.ws.rest.code;

public class Codigo {
	private String codigo;
	private boolean verificadorValido;
	
	public String getCodigo() {
		return codigo;
	}
	
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	public boolean isVerificadorValido() {
		return verificadorValido;
	}
	
	public void setVerificadorValido(boolean verificadorValido) {
		this.verificadorValido = verificadorValido;
	}
}